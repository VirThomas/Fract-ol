#include "libft.h"

char	*ft_strrev(char *str)
{
	char	tmp;
	int		len;
	int		i;

	i = 0;
	len = 0;
	while (str[len])
		len++;
	len--;
	while (i < len)
	{
		tmp = str[i];
		str[i] = str[len];
		str[len] = tmp;
		i++;
		len--;
	}
	return (str);
}
